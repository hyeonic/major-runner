<template>
  <div>
    <loading-spinner v-if="isLoading"></loading-spinner>
    <div v-else>
      <div class="title">검색 결과</div>
    </div>

    <ul v-if="posts.length !== 0">
      <post-list-item
        v-for="(post, index) in posts"
        :key="index"
        :post="post"
        @refresh="fetchPosts"
      ></post-list-item>
    </ul>
    <div class="log-message" v-else>
      {{ logMessage }}
    </div>
    <div class="footer">
      <button @click="decrementPage" :disabled="pageble.page === 0">
        이전
      </button>
      <div>{{ pageble.page + 1 }} / {{ totalPages }}</div>
      <button
        @click="incrementPage"
        :disabled="pageble.page === totalPages - 1"
      >
        다음
      </button>
    </div>
  </div>
</template>

<script>
import PostListItem from '@/components/list/PostListItem.vue';
import LoadingSpinner from '@/components/common/LoadingSpinner.vue';
import { searchPosts } from '@/api/posts.js';

export default {
  data() {
    return {
      title: '',
      posts: [],
      pageble: {
        page: 0,
        size: 10,
        sort: 'createdAt',
      },
      totalPages: 0,
      logMessage: '',
      isLoading: false,
    };
  },
  created() {
    this.fetchPosts();
  },
  methods: {
    async fetchPosts() {
      try {
        this.isLoading = true;
        this.title = this.$route.params.title;
        const { data } = await searchPosts(this.title, this.pageble);
        this.isLoading = false;
        this.totalPages = data.page.totalPages;
        this.posts = data._embedded.postList;
      } catch (error) {
        this.logMessage = '검색 결과가 없습니다.';
      }
    },
    incrementPage() {
      this.pageble.page += 1;
      this.fetchPosts();
    },
    decrementPage() {
      this.pageble.page -= 1;
      this.fetchPosts();
    },
  },
  components: {
    PostListItem,
    LoadingSpinner,
  },
};
</script>

